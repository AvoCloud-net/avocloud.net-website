function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if (_i18nData[lang] && _i18nData[lang][key] !== undefined) {
      el.textContent = _i18nData[lang][key];
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.dataset.i18nHtml;
    if (_i18nData[lang] && _i18nData[lang][key] !== undefined) {
      el.innerHTML = _i18nData[lang][key];
    }
  });
  var btn = document.getElementById('lang-btn');
  if (btn) {
    btn.textContent = lang === 'en' ? 'DE' : 'EN';
    btn.title = lang === 'en' ? 'Auf Deutsch wechseln' : 'Switch to English';
  }
  document.documentElement.lang = lang === 'en' ? 'en' : 'de';
  localStorage.setItem('avo-lang', lang);
}

function toggleLang() {
  var current = localStorage.getItem('avo-lang') || 'en';
  applyLang(current === 'en' ? 'de' : 'en');
}

document.addEventListener('DOMContentLoaded', function() {
  applyLang(localStorage.getItem('avo-lang') || 'en');
});
